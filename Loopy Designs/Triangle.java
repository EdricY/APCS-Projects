public class Triangle {
  public static void main(String[] args) { 
    final int LENGTH = 11;

    for (int i = 1; i <= LENGTH; i++) 
    {	
      for (int j = 1; j <= i; j++) 
        System.out.print("*"); 
    System.out.println("");
    }
   }
}
